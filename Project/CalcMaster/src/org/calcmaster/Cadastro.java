    private void btnRegActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegActionPerformed
        int result = calcCon.addUser(txtName.getText(),
        txtEmail.getText(), txtCPF.getText(), txtRegPassword.getText());
        if (result ==1)
        JOptionPane.showMessageDialog(this, "Usuário cadastrado", "Usuário Cadastrado", JOptionPane.PLAIN_MESSAGE);
        else
        JOptionPane.showMessageDialog(this, "Falha no cadastro", "Erro", JOptionPane.PLAIN_MESSAGE);
    }//GEN-LAST:event_btnRegActionPerformed

